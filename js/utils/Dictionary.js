/**
 * 词典模块 - 提供单词验证和查询功能
 */
const Dictionary = {
    /**
     * 检查单词是否存在于词典中
     * @param {string} word - 要检查的单词
     * @returns {boolean} - 如果单词存在返回true
     */
    has(word) {
        const upperWord = word.toUpperCase();
        return getAllWords().hasOwnProperty(upperWord);
    },

    /**
     * 获取单词的中文释义
     * @param {string} word - 要查询的单词
     * @returns {string|null} - 中文释义，不存在则返回null
     */
    getMeaning(word) {
        const upperWord = word.toUpperCase();
        const allWords = getAllWords();
        return allWords[upperWord] || null;
    },

    /**
     * 获取所有单词数据（用于词库展示）
     * @returns {Object} - 所有单词及其释义
     */
    getAllWords() {
        return getAllWords();
    },

    /**
     * 按单词长度筛选
     * @param {number} length - 单词长度
     * @returns {Object} - 符合长度的单词
     */
    getWordsByLength(length) {
        const allWords = getAllWords();
        const result = {};
        for (const [word, meaning] of Object.entries(allWords)) {
            if (word.length === length) {
                result[word] = meaning;
            }
        }
        return result;
    },

    /**
     * 搜索单词（支持模糊搜索和长度筛选）
     * @param {string} query - 搜索关键词
     * @param {number|null} length - 单词长度筛选 (可选)
     * @returns {Object} - 匹配的单词
     */
    search(query, length = null) {
        const upperQuery = query.toUpperCase();
        const allWords = getAllWords();
        const result = {};
        for (const [word, meaning] of Object.entries(allWords)) {
            // 长度筛选
            if (length && word.length !== parseInt(length)) {
                continue;
            }

            // 匹配单词或中文释义
            if (word.includes(upperQuery) || meaning.includes(query)) {
                result[word] = meaning;
            }
        }
        return result;
    },

    /**
     * 获取词典统计信息
     * @returns {Object} - 统计信息
     */
    getStats() {
        const allWords = getAllWords();
        const total = Object.keys(allWords).length;
        const byLength = {};
        for (const word of Object.keys(allWords)) {
            const len = word.length;
            byLength[len] = (byLength[len] || 0) + 1;
        }
        return { total, byLength };
    }
};

export default Dictionary;

